create or replace function public.mobile_register_device(
  p_session_token text,
  p_device jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_owner text;
  v_device_id text;
begin
  v_owner := public.mobile_session_owner(p_session_token);
  if v_owner is null then
    raise exception using errcode = 'P0001', message = 'Sessao mobile invalida ou expirada.';
  end if;

  v_device_id := nullif(trim(p_device->>'id'), '');
  if v_device_id is null then
    raise exception using errcode = '22023', message = 'Dispositivo invalido.';
  end if;

  insert into public.mobile_dispositivos (
    id,
    usuario_id,
    plataforma,
    modelo,
    fabricante,
    app_version,
    ultimo_sync_em,
    created_at,
    updated_at
  ) values (
    v_device_id,
    v_owner,
    nullif(trim(p_device->>'plataforma'), ''),
    nullif(trim(p_device->>'modelo'), ''),
    nullif(trim(p_device->>'fabricante'), ''),
    nullif(trim(p_device->>'app_version'), ''),
    now(),
    coalesce(nullif(p_device->>'created_at', '')::timestamptz, now()),
    now()
  ) on conflict (id) do update set
    usuario_id = excluded.usuario_id,
    plataforma = excluded.plataforma,
    modelo = excluded.modelo,
    fabricante = excluded.fabricante,
    app_version = excluded.app_version,
    ultimo_sync_em = excluded.ultimo_sync_em,
    updated_at = excluded.updated_at;

  update public.mobile_sessions
  set dispositivo_id = v_device_id, ultimo_uso_em = now()
  where token_hash = encode(digest(coalesce(p_session_token, ''), 'sha256'), 'hex');

  return jsonb_build_object('registered', true, 'device_id', v_device_id);
end;
$$;

