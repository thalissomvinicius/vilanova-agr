create or replace function public.audit_field_deposit_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_id uuid;
  v_actor text;
begin
  v_id := case when tg_op = 'DELETE' then old.id else new.id end;
  v_actor := coalesce(
    nullif(current_setting('app.actor_label', true), ''),
    case when tg_op = 'DELETE' then old.reviewed_by_label else new.reviewed_by_label end,
    nullif(current_setting('request.jwt.claim.sub', true), ''),
    'sistema'
  );
  insert into public.field_deposit_audit (
    field_deposit_id,
    acao,
    actor_label,
    old_data,
    new_data
  ) values (
    v_id,
    lower(tg_op),
    v_actor,
    case when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old) - 'dump_photo_data_url' else null end,
    case when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new) - 'dump_photo_data_url' else null end
  );

  return coalesce(new, old);
end;
$$;

