create or replace function public.mobile_sync_bundle(
  p_session_token text,
  p_respostas jsonb default '[]'::jsonb,
  p_gps jsonb default '[]'::jsonb,
  p_anexos jsonb default '[]'::jsonb,
  p_deposits jsonb default '[]'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_owner text;
  v_device_id text;
  v_response_count integer := 0;
  v_gps_count integer := 0;
  v_attachment_count integer := 0;
  v_deposit_result jsonb := jsonb_build_object('synced', 0, 'skipped', 0);
begin
  v_owner := public.mobile_session_owner(p_session_token);
  if v_owner is null then
    raise exception using errcode = 'P0001', message = 'Sessao mobile invalida ou expirada.';
  end if;

  select dispositivo_id into v_device_id
  from public.mobile_sessions
  where token_hash = encode(digest(coalesce(p_session_token, ''), 'sha256'), 'hex');

  if jsonb_typeof(coalesce(p_respostas, '[]'::jsonb)) <> 'array'
    or jsonb_typeof(coalesce(p_gps, '[]'::jsonb)) <> 'array'
    or jsonb_typeof(coalesce(p_anexos, '[]'::jsonb)) <> 'array'
    or jsonb_typeof(coalesce(p_deposits, '[]'::jsonb)) <> 'array'
    or jsonb_array_length(coalesce(p_respostas, '[]'::jsonb)) > 25
    or jsonb_array_length(coalesce(p_gps, '[]'::jsonb)) > 100
    or jsonb_array_length(coalesce(p_anexos, '[]'::jsonb)) > 50
    or jsonb_array_length(coalesce(p_deposits, '[]'::jsonb)) > 25 then
    raise exception using errcode = '22023', message = 'Lote de sincronizacao invalido ou acima do limite.';
  end if;

  if exists (
    select 1
    from jsonb_array_elements(coalesce(p_respostas, '[]'::jsonb)) item
    where coalesce(nullif(item->>'usuario_id', ''), v_owner) <> v_owner
  ) then
    raise exception using errcode = '42501', message = 'A coleta nao pertence a sessao informada.';
  end if;

  insert into public.mobile_respostas (
    id,
    formulario_id,
    formulario_versao,
    usuario_id,
    dados_json,
    mapeamento_legado,
    status,
    dispositivo_id,
    origem,
    criado_em,
    enviado_em,
    recebido_em,
    atualizado_em,
    updated_at,
    erro_msg,
    tentativas
  )
  select
    row.id,
    row.formulario_id,
    row.formulario_versao,
    v_owner,
    row.dados_json,
    row.mapeamento_legado,
    coalesce(nullif(row.status, ''), 'pendente_validacao'),
    coalesce(nullif(row.dispositivo_id, ''), v_device_id),
    coalesce(nullif(row.origem, ''), 'app_android'),
    coalesce(row.criado_em, now()),
    coalesce(row.enviado_em, now()),
    now(),
    coalesce(row.updated_at, row.criado_em, now()),
    coalesce(row.updated_at, row.criado_em, now()),
    row.erro_msg,
    coalesce(row.tentativas, 0)
  from jsonb_to_recordset(coalesce(p_respostas, '[]'::jsonb)) as row(
    id text,
    formulario_id text,
    formulario_versao integer,
    usuario_id text,
    dados_json jsonb,
    mapeamento_legado jsonb,
    status text,
    dispositivo_id text,
    origem text,
    criado_em timestamptz,
    enviado_em timestamptz,
    updated_at timestamptz,
    erro_msg text,
    tentativas integer
  )
  where row.id is not null
    and row.formulario_id is not null
    and row.dados_json is not null
  on conflict (id) do update set
    formulario_versao = excluded.formulario_versao,
    dados_json = excluded.dados_json,
    mapeamento_legado = excluded.mapeamento_legado,
    status = excluded.status,
    dispositivo_id = excluded.dispositivo_id,
    origem = excluded.origem,
    enviado_em = excluded.enviado_em,
    atualizado_em = excluded.atualizado_em,
    updated_at = excluded.updated_at,
    erro_msg = excluded.erro_msg,
    tentativas = excluded.tentativas
  where excluded.usuario_id = v_owner
    and excluded.updated_at >= coalesce(public.mobile_respostas.updated_at, '-infinity'::timestamptz);

  get diagnostics v_response_count = row_count;

  if exists (
    select 1
    from jsonb_array_elements(coalesce(p_deposits, '[]'::jsonb)) item
    left join public.mobile_respostas response
      on response.id = item->>'source_response_id'
     and response.usuario_id = v_owner
    where nullif(item->>'source_response_id', '') is null
       or response.id is null
  ) then
    raise exception using errcode = '42501', message = 'O despejo nao pertence a uma coleta valida da sessao.';
  end if;

  if exists (
    select 1
    from jsonb_array_elements(coalesce(p_deposits, '[]'::jsonb)) item
    where nullif(trim(item->>'driver_registration'), '') is null
       or nullif(trim(item->>'driver_name'), '') is null
       or nullif(trim(item->>'vehicle_plate'), '') is null
       or nullif(trim(item->>'subproduct'), '') is null
       or nullif(trim(item->>'loading_origin'), '') is null
       or nullif(trim(item->>'scale_ticket_code'), '') is null
       or nullif(trim(item->>'farm'), '') is null
       or nullif(trim(item->>'plot_primary'), '') is null
       or nullif(trim(item->>'deposit_date'), '') is null
       or nullif(trim(item->>'deposit_time'), '') is null
       or nullif(trim(item->>'latitude'), '') is null
       or nullif(trim(item->>'longitude'), '') is null
       or nullif(trim(item->>'dump_photo_storage_path'), '') is null
       or nullif(trim(item->>'dump_photo_latitude'), '') is null
       or nullif(trim(item->>'dump_photo_longitude'), '') is null
       or item->>'dump_photo_storage_path' not like v_owner || '/%'
       or (
         lower(trim(coalesce(item->>'placement_mode', ''))) = 'between_plots'
         and nullif(trim(item->>'plot_secondary'), '') is null
       )
  ) then
    raise exception using errcode = '22023', message = 'Coleta obrigatoria incompleta ou foto sem autoria valida.';
  end if;

  insert into public.mobile_gps (
    id,
    resposta_id,
    campo_id,
    latitude,
    longitude,
    precisao,
    altitude,
    capturado_em
  )
  select
    row.id,
    row.resposta_id,
    row.campo_id,
    row.latitude,
    row.longitude,
    row.precisao,
    row.altitude,
    coalesce(row.capturado_em, now())
  from jsonb_to_recordset(coalesce(p_gps, '[]'::jsonb)) as row(
    id text,
    resposta_id text,
    campo_id text,
    latitude double precision,
    longitude double precision,
    precisao double precision,
    altitude double precision,
    capturado_em timestamptz
  )
  join public.mobile_respostas response
    on response.id = row.resposta_id
   and response.usuario_id = v_owner
  where row.id is not null and row.campo_id is not null
  on conflict (id) do update set
    latitude = excluded.latitude,
    longitude = excluded.longitude,
    precisao = excluded.precisao,
    altitude = excluded.altitude,
    capturado_em = excluded.capturado_em;

  get diagnostics v_gps_count = row_count;

  insert into public.mobile_anexos (
    id,
    resposta_id,
    campo_id,
    storage_path,
    nome_arquivo,
    tamanho_bytes,
    tipo_mime,
    criado_em
  )
  select
    row.id,
    row.resposta_id,
    row.campo_id,
    row.storage_path,
    row.nome_arquivo,
    row.tamanho_bytes,
    row.tipo_mime,
    coalesce(row.criado_em, now())
  from jsonb_to_recordset(coalesce(p_anexos, '[]'::jsonb)) as row(
    id text,
    resposta_id text,
    campo_id text,
    storage_path text,
    nome_arquivo text,
    tamanho_bytes bigint,
    tipo_mime text,
    criado_em timestamptz
  )
  join public.mobile_respostas response
    on response.id = row.resposta_id
   and response.usuario_id = v_owner
  where row.id is not null
    and row.campo_id is not null
    and row.storage_path like v_owner || '/%'
  on conflict (id) do update set
    storage_path = excluded.storage_path,
    nome_arquivo = excluded.nome_arquivo,
    tamanho_bytes = excluded.tamanho_bytes,
    tipo_mime = excluded.tipo_mime,
    criado_em = excluded.criado_em;

  get diagnostics v_attachment_count = row_count;

  if jsonb_array_length(coalesce(p_deposits, '[]'::jsonb)) > 0 then
    v_deposit_result := public.mobile_sync_subproduct_deposits(p_deposits);
  end if;

  update public.mobile_sessions
  set ultimo_uso_em = now()
  where token_hash = encode(digest(coalesce(p_session_token, ''), 'sha256'), 'hex');

  insert into public.mobile_audit_log (
    matricula,
    dispositivo_id,
    acao,
    entidade,
    detalhes
  ) values (
    v_owner,
    v_device_id,
    'sync_bundle',
    'mobile_respostas',
    jsonb_build_object(
      'responses', v_response_count,
      'gps', v_gps_count,
      'attachments', v_attachment_count,
      'deposits', coalesce((v_deposit_result->>'synced')::integer, 0)
    )
  );

  return jsonb_build_object(
    'responses', v_response_count,
    'gps', v_gps_count,
    'attachments', v_attachment_count,
    'deposits', coalesce((v_deposit_result->>'synced')::integer, 0),
    'deposit_result', v_deposit_result
  );
end;
$$;

