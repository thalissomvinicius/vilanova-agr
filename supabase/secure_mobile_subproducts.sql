-- Execute depois de schema.sql, dashboard_subprodutos_rpc.sql e mobile_subproduct_bridge.sql.
-- Seguro para reexecutar. Nao apaga coletas existentes.

begin;

create extension if not exists pgcrypto;

alter table public.headcount_colaboradores
  add column if not exists senha_hash text;

update public.headcount_colaboradores
set senha_hash = case
  when coalesce(senha_hash, '') <> '' then senha_hash
  when coalesce(senha, '') like '$2%' then senha
  else crypt(coalesce(nullif(senha, ''), '1234'), gen_salt('bf', 10))
end
where coalesce(senha_hash, '') = '';

alter table public.headcount_colaboradores
  alter column senha drop default,
  alter column senha drop not null;

-- A senha deixa de ser retornavel. O hash fica acessivel apenas dentro das funcoes SECURITY DEFINER.
update public.headcount_colaboradores set senha = null where senha is not null;

create table if not exists public.mobile_sessions (
  token_hash text primary key,
  matricula text not null references public.headcount_colaboradores(matricula) on delete cascade,
  dispositivo_id text,
  criado_em timestamptz not null default now(),
  ultimo_uso_em timestamptz not null default now(),
  expira_em timestamptz not null default (now() + interval '30 days'),
  revogado_em timestamptz
);

create index if not exists mobile_sessions_matricula_idx
  on public.mobile_sessions (matricula, expira_em desc);

create table if not exists public.mobile_login_attempts (
  id bigint generated by default as identity primary key,
  matricula text not null,
  sucesso boolean not null,
  criado_em timestamptz not null default now()
);

create index if not exists mobile_login_attempts_recent_idx
  on public.mobile_login_attempts (matricula, criado_em desc);

create table if not exists public.mobile_audit_log (
  id bigint generated by default as identity primary key,
  matricula text,
  dispositivo_id text,
  acao text not null,
  entidade text,
  entidade_id text,
  detalhes jsonb,
  criado_em timestamptz not null default now()
);

create index if not exists mobile_audit_log_created_idx
  on public.mobile_audit_log (criado_em desc);

create table if not exists public.field_deposit_audit (
  id bigint generated by default as identity primary key,
  field_deposit_id uuid,
  acao text not null,
  actor_label text,
  old_data jsonb,
  new_data jsonb,
  criado_em timestamptz not null default now()
);

create index if not exists field_deposit_audit_deposit_idx
  on public.field_deposit_audit (field_deposit_id, criado_em desc);

alter table public.mobile_sessions enable row level security;
alter table public.mobile_login_attempts enable row level security;
alter table public.mobile_audit_log enable row level security;
alter table public.field_deposit_audit enable row level security;
