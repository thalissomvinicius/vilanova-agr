create or replace function public.mobile_session_owner(p_session_token text)
returns text
language sql
security definer
stable
set search_path = public, extensions
as $$
  select session.matricula
  from public.mobile_sessions session
  join public.headcount_colaboradores collaborator
    on collaborator.matricula = session.matricula
   and collaborator.status = 'ATIVO'
  where session.token_hash = encode(digest(coalesce(p_session_token, ''), 'sha256'), 'hex')
    and session.revogado_em is null
    and session.expira_em > now()
  limit 1;
$$;

create or replace function public.mobile_authenticate(
  p_matricula text,
  p_senha text,
  p_dispositivo_id text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_matricula text := regexp_replace(coalesce(p_matricula, ''), '[^0-9]', '', 'g');
  v_collaborator public.headcount_colaboradores%rowtype;
  v_token text;
  v_recent_failures integer;
begin
  delete from public.mobile_login_attempts
  where criado_em < now() - interval '30 days';

  delete from public.mobile_sessions
  where expira_em <= now() or revogado_em is not null;

  select count(*)
  into v_recent_failures
  from public.mobile_login_attempts
  where matricula = v_matricula
    and sucesso = false
    and criado_em > now() - interval '15 minutes';

  if v_recent_failures >= 8 then
    raise exception using
      errcode = 'P0001',
      message = 'Muitas tentativas. Aguarde alguns minutos e tente novamente.';
  end if;

  select *
  into v_collaborator
  from public.headcount_colaboradores
  where matricula = v_matricula
    and status = 'ATIVO'
  limit 1;

  if v_collaborator.matricula is null
    or coalesce(v_collaborator.senha_hash, '') = ''
    or crypt(coalesce(p_senha, ''), v_collaborator.senha_hash) <> v_collaborator.senha_hash then
    insert into public.mobile_login_attempts (matricula, sucesso)
    values (coalesce(nullif(v_matricula, ''), 'desconhecida'), false);

    return jsonb_build_object('authenticated', false);
  end if;

  insert into public.mobile_login_attempts (matricula, sucesso)
  values (v_collaborator.matricula, true);

  v_token := encode(gen_random_bytes(32), 'hex');

  insert into public.mobile_sessions (
    token_hash,
    matricula,
    dispositivo_id,
    criado_em,
    ultimo_uso_em,
    expira_em
  ) values (
    encode(digest(v_token, 'sha256'), 'hex'),
    v_collaborator.matricula,
    nullif(trim(coalesce(p_dispositivo_id, '')), ''),
    now(),
    now(),
    now() + interval '30 days'
  );

  insert into public.mobile_usuarios (
    id,
    matricula,
    nome,
    cargo,
    departamento,
    gestor,
    ultimo_login_em,
    updated_at
  ) values (
    v_collaborator.matricula,
    v_collaborator.matricula,
    v_collaborator.nome,
    v_collaborator.cargo,
    v_collaborator.departamento,
    v_collaborator.gestor,
    now(),
    now()
  ) on conflict (id) do update set
    nome = excluded.nome,
    cargo = excluded.cargo,
    departamento = excluded.departamento,
    gestor = excluded.gestor,
    ultimo_login_em = excluded.ultimo_login_em,
    updated_at = excluded.updated_at;

  insert into public.mobile_audit_log (matricula, dispositivo_id, acao, entidade, entidade_id)
  values (v_collaborator.matricula, p_dispositivo_id, 'login', 'mobile_session', v_collaborator.matricula);

  return jsonb_build_object(
    'authenticated', true,
    'session_token', v_token,
    'expires_at', now() + interval '30 days',
    'matricula', v_collaborator.matricula,
    'nome', v_collaborator.nome,
    'departamento', v_collaborator.departamento,
    'cargo', v_collaborator.cargo,
    'gestor', v_collaborator.gestor,
    'status', v_collaborator.status
  );
end;
$$;

create or replace function public.mobile_session_profile(p_session_token text)
returns table (
  matricula text,
  nome text,
  departamento text,
  cargo text,
  gestor text,
  status text,
  expires_at timestamptz
)
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_owner text;
begin
  v_owner := public.mobile_session_owner(p_session_token);
  if v_owner is null then
    return;
  end if;

  update public.mobile_sessions
  set ultimo_uso_em = now()
  where token_hash = encode(digest(coalesce(p_session_token, ''), 'sha256'), 'hex');

  return query
  select
    collaborator.matricula,
    collaborator.nome,
    collaborator.departamento,
    collaborator.cargo,
    collaborator.gestor,
    collaborator.status,
    session.expira_em
  from public.headcount_colaboradores collaborator
  join public.mobile_sessions session on session.matricula = collaborator.matricula
  where collaborator.matricula = v_owner
    and session.token_hash = encode(digest(coalesce(p_session_token, ''), 'sha256'), 'hex')
  limit 1;
end;
$$;

create or replace function public.mobile_logout(p_session_token text)
returns boolean
language plpgsql
security definer
set search_path = public, extensions
as $$
begin
  update public.mobile_sessions
  set revogado_em = now()
  where token_hash = encode(digest(coalesce(p_session_token, ''), 'sha256'), 'hex')
